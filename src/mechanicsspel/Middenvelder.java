package mechanicsspel;


public class Middenvelder extends Speler{
	
	public Middenvelder(String nm, int pr, String tp, int off, int def, int uit, String st){
		super(nm,pr,tp,off,def,uit,st);
	}
	
	/**
	 * string representatie van een middenvelder, gebruikt toString methode van Speler
	 */
	public String toString(){
		return super.toString();
	}
	
	/**
	 * XML representatie van een middenvelder, gebruikt toString methode van Speler
	 */
	public String toXML(){
		return super.toXML();
	}
}
